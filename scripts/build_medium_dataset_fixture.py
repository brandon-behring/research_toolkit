"""Build tests/fixtures/medium_dataset_subset/ from the time-series-anomaly dogfood.

Filters the 45-entry full dossier to ~12 entries spanning 4+ source categories,
producing a self-contained fixture that demonstrates the v1.6 schema with
full optional-field coverage. Mirrors `scripts/build_medium_fixture.py`
(v1.3 paper-pipeline) but for the dataset pipeline.

Idempotent — re-running rebuilds from current source state.
"""
from __future__ import annotations

import shutil
import sys
from pathlib import Path

import yaml

REPO_ROOT = Path(__file__).resolve().parent.parent
SOURCE_LEDGER = (
    Path.home() / "Claude" / "research_time_series_anomaly" / "dataset_ledger.yml"
)
SOURCE_AGENT_INDEX = (
    Path.home() / "interview_prep_series" / "docs" / "research"
    / "time_series_anomaly_datasets"
)
OUT = REPO_ROOT / "tests" / "fixtures" / "medium_dataset_subset"

# Curated subset: ~12 entries spanning 4 source categories with
# full v1.6 schema coverage (license, size, schema_url where available).
# Picks canonical landmark datasets a future agent / human reader would
# expect to find in any time-series anomaly survey.
KEEP_BIBKEYS = {
    # github (5) — canonical landmarks
    "nab2017numenta",          # Numenta Anomaly Benchmark
    "skab2020skoltech",        # SKAB
    "tods2021lai",              # TODS toolkit
    "msl2018telemanom",        # MSL/SMAP NASA telemetry
    "smd2019omnianomaly",      # Server Machine Dataset
    # huggingface (3)
    "timeseriespile2024autonlab",  # MOMENT TS Pile corpus
    "catshf2023fleith",         # CATS HuggingFace mirror
    "continualenergy2024lifelonglab",  # continual-learning energy
    # zenodo (2)
    "mgab2020thill",            # Mackey-Glass Anomaly Benchmark
    "cats2023fleith",           # CATS Zenodo canonical
    # uci/aws (2)
    "ucr2018archive",           # UCR Time Series Archive
    "nyctlc2014aws",            # NYC TLC on AWS Open Data
}


def main() -> int:
    if not SOURCE_LEDGER.is_file():
        print(f"FATAL: {SOURCE_LEDGER} not found", file=sys.stderr)
        return 2
    if not SOURCE_AGENT_INDEX.is_dir():
        print(f"FATAL: {SOURCE_AGENT_INDEX} not found", file=sys.stderr)
        return 2

    if OUT.exists():
        shutil.rmtree(OUT)
    OUT.mkdir(parents=True)

    # 1. Filter the ledger.
    src_text = SOURCE_LEDGER.read_text(encoding="utf-8")
    data = yaml.safe_load(src_text)
    entries = [
        e for e in data["entries"]
        if isinstance(e, dict) and e.get("bibkey") in KEEP_BIBKEYS
    ]
    print(f"Filtered {len(entries)} of {len(KEEP_BIBKEYS)} target entries", file=sys.stderr)
    missing = KEEP_BIBKEYS - {e["bibkey"] for e in entries}
    if missing:
        print(f"  WARN: {len(missing)} target bibkeys not found in source: {sorted(missing)}", file=sys.stderr)

    lines = [
        "# Medium fixture: time-series anomaly dataset subset",
        "# Generated by scripts/build_medium_dataset_fixture.py — do not hand-edit.",
        f"# {len(entries)} entries spanning github / huggingface / zenodo / uci+aws",
        "# with full v1.6 optional-field coverage.",
        "",
        "entries:",
    ]
    for e in entries:
        for k in (
            "bibkey", "primary_url", "name", "source", "status", "task_family",
            "license", "size", "rows", "columns", "schema_url",
            "access_method", "auth_required", "citation",
        ):
            if k not in e:
                continue
            v = e[k]
            if isinstance(v, bool):
                v_str = str(v).lower()
            elif isinstance(v, (int, float)):
                v_str = str(v)
            else:
                v_str = _yaml_scalar(str(v))
            if k == "bibkey":
                lines.append(f"- {k}: {v_str}")
            else:
                lines.append(f"  {k}: {v_str}")
        lines.append("")
    (OUT / "dataset_ledger.yml").write_text("\n".join(lines), encoding="utf-8")

    # 2. Build minimal agent_index/ — single combined synthesis file is enough
    # for the fixture (production usage splits by source category, but the
    # fixture exists to demonstrate the schema, not the splitting logic).
    (OUT / "agent_index").mkdir()

    synthesis = ["# Time-series anomaly datasets — fixture subset",
                 "",
                 "## A1. Curated subset",
                 ""]
    for e in entries:
        name = e["name"]
        source = e.get("source", "?")
        access_method = e.get("access_method", "?")
        auth_required = "Y" if e.get("auth_required") else "N"
        size = e.get("size", "?")
        license_ = e.get("license", "?")
        synthesis.append(f"- **{name}** — {source}.")
        synthesis.append(f"  - **Source:** {e['primary_url']}")
        synthesis.append(f"  - **Access:** {access_method}; auth_required: {auth_required}")
        if e.get("schema_url"):
            synthesis.append(f"  - **Schema:** schema_url: {e['schema_url']}")
        else:
            synthesis.append(f"  - **Schema:** see dataset card / source page")
        synthesis.append(f"  - **Size+License:** {size}; {license_}")
        synthesis.append(f"  - **Tasks:** time-series anomaly detection")
        synthesis.append(f"  - **Status:** Verified.")
        synthesis.append("")
    (OUT / "agent_index" / "01_subset.md").write_text("\n".join(synthesis), encoding="utf-8")

    # README satisfying the agent_index validator.
    readme = f"""<!-- AGENT-INDEX: medium dataset fixture — time-series anomaly subset, {len(entries)} entries. Read this README first. -->

# Time-Series Anomaly Datasets — Medium Fixture

**Purpose:** v1.6 dataset-pipeline schema reference; test fixture for `dataset_ledger` validator + dataset-index sub-section logic.
**Scope:** {len(entries)} canonical landmark datasets spanning github / huggingface / zenodo / uci+aws.
**Coverage:** all entries have license, size, access_method, auth_required populated; schema_url where the source provides one.

## ⚠️ Scope boundary

This fixture is a SUBSET of the full time-series anomaly dataset dossier. It exists to test the v1.6 schema; for the full ~45-entry dossier with all source categories, see the production artifact in `interview_prep_series`.

## How this is organized

| File | Contents |
|---|---|
| `01_subset.md` | curated {len(entries)}-entry subset, single sub-section (A1) |

## Lookup recipes

- **"What's the canonical streaming-anomaly benchmark?"** → `01_subset.md` § A1 (NAB / Numenta).
- **"Which anomaly dataset has the most permissive license?"** → `01_subset.md` § A1 (look for MIT / CC-BY / public domain).
- **"What's the standard NASA telemetry anomaly dataset?"** → `01_subset.md` § A1 (MSL/SMAP / telemanom).
- **"Where do I get the UCR archive?"** → `01_subset.md` § A1.

## Glossary

- **NAB** — Numenta Anomaly Benchmark; canonical streaming-anomaly benchmark.
- **MSL/SMAP** — NASA spacecraft telemetry anomaly datasets.
- **UCR archive** — Eamonn Keogh's time-series classification + anomaly archive (UC Riverside).
- **MGAB** — Mackey-Glass Anomaly Benchmark.

## Verification & limits

- Citations resolved as of 2026-05-08 (sourced from the v1.6 dogfood).
- This fixture is a snapshot subset; do not use as the canonical anomaly dataset reference.

## Attribution

Generated by `scripts/build_medium_dataset_fixture.py` from `~/Claude/research_time_series_anomaly/`.
"""
    (OUT / "agent_index" / "README.md").write_text(readme, encoding="utf-8")

    # research_plan.md — minimal stub satisfying validator (4-8 sub-areas, 1+ out-of-scope, 3+ taxonomy).
    plan = """# Research Plan: Time-Series Anomaly Datasets (Medium Fixture)

A scoped subset of the time-series anomaly detection dataset dossier. Used as a v1.6 schema reference and test fixture (~12 entries).

## Sub-areas

- A1. Streaming benchmarks
  - Source types: github, huggingface
  - Notes: NAB-shaped streaming anomaly with labeled timestamps.
- A2. Multivariate sensor benchmarks
  - Source types: github, zenodo
  - Notes: spacecraft / industrial / environmental sensor multivariate.
- A3. Classification / clustering archives
  - Source types: uci
  - Notes: UCR-style time-series archives that contain anomaly subsets.
- A4. Cloud / public datasets
  - Source types: aws_open_data
  - Notes: NYC TLC and similar public-cloud-hosted time-series datasets.

## Out-of-scope

- Forecasting-only datasets (no anomaly labels).
- Synthetic-only generators without a published benchmark.
- Datasets with non-redistributable licenses requiring special agreement.

## Claim family taxonomy

- time_series
- tabular
- multimodal
"""
    (OUT / "research_plan.md").write_text(plan, encoding="utf-8")

    print(f"Wrote medium dataset fixture to {OUT}", file=sys.stderr)
    print(f"  {len(entries)} entries", file=sys.stderr)
    return 0


def _yaml_scalar(s: str) -> str:
    if any(c in s for c in [":", "#", "'", '"', "[", "]", "{", "}", ","]):
        escaped = s.replace("'", "''")
        return f"'{escaped}'"
    return s


if __name__ == "__main__":
    sys.exit(main())
